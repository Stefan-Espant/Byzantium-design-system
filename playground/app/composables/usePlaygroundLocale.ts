export function usePlaygroundLocale() {
  const { currentKey } = useLocale()

  function p(key: keyof PlaygroundStrings): string {
    const lang = currentKey.value as LocaleKey
    return (translations[lang] ?? translations.nl)[key]
  }

  return { p, translations }
}
