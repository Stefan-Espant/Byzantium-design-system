import { readdir, readFile, writeFile, mkdir, copyFile } from 'node:fs/promises'
import { join, dirname, basename, extname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const repoRoot = join(__dirname, '..')
const lucideIconsDir = join(
  repoRoot,
  'node_modules/.pnpm/lucide-vue-next@1.0.0_vue@3.5.35_typescript@5.9.3_/node_modules/lucide-vue-next/dist/esm/icons',
)
const customIconsDir = join(__dirname, 'custom-icons')
const outputDir = join(
  repoRoot,
  'packages/byzantium/src/components/primitives/ByzIcon',
)

function escapeAttribute(value) {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('"', '&quot;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
}

function toCamelCase(value) {
  return value.replace(/-([a-z0-9])/g, (_, char) => char.toUpperCase())
}

async function loadIcons() {
  const files = (await readdir(lucideIconsDir))
    .filter((file) => file.endsWith('.js') && !file.endsWith('.map'))
    .sort()

  const icons = []

  for (const file of files) {
    const source = await readFile(join(lucideIconsDir, file), 'utf8')
    const match = source.match(/createLucideIcon\("([^"]+)",\s*(\[[\s\S]*?\])\s*\);/)

    if (!match) continue

    const [, iconName, iconNodeSource] = match
    const iconNodes = Function(`"use strict"; return (${iconNodeSource});`)()
    const body = iconNodes
      .map(([tag, attrs]) => {
        const serializedAttrs = Object.entries(attrs)
          .filter(([key]) => key !== 'key')
          .map(([key, value]) => `${key}="${escapeAttribute(value)}"`)
          .join(' ')

        return `<${tag} ${serializedAttrs}></${tag}>`
      })
      .join('')

    icons.push({
      name: iconName,
      camelName: toCamelCase(iconName),
      body,
    }) 
  }

  return icons
}

async function writeSprite(icons) {
  const sprite = [
    '<svg xmlns="http://www.w3.org/2000/svg" style="display:none">',
    ...icons.map(
      (icon) => `<symbol id="byz-icon-${icon.name}" viewBox="0 0 24 24">${icon.body}</symbol>`,
    ),
    '</svg>',
    '',
  ].join('\n')

  await writeFile(join(outputDir, 'byz-icons-sprite.svg'), sprite, 'utf8')
}

async function writeNamesModule(icons) {
  const namesModule = `// Generated by scripts/generate-icon-sprite.mjs
export const generatedIconNames = ${JSON.stringify(icons.map((icon) => icon.name), null, 2)} as const

export type GeneratedIconName = typeof generatedIconNames[number]
`

  await writeFile(join(outputDir, 'icon-names.generated.ts'), namesModule, 'utf8')
}

async function loadCustomIcons() {
  let files
  try {
    files = (await readdir(customIconsDir)).filter((f) => f.endsWith('.svg')).sort()
  } catch {
    return []
  }

  const icons = []
  for (const file of files) {
    const name = basename(file, extname(file))
    const source = await readFile(join(customIconsDir, file), 'utf8')
    const inner = source.replace(/<svg[^>]*>/, '').replace(/<\/svg>/, '').trim()
    icons.push({ name, body: inner })
  }
  return icons
}

const playgroundPublicDir = join(repoRoot, 'playground/public')

await mkdir(outputDir, { recursive: true })
await mkdir(playgroundPublicDir, { recursive: true })

const lucideIcons = await loadIcons()
const customIcons = await loadCustomIcons()

const knownNames = new Set(lucideIcons.map((i) => i.name))
const uniqueCustom = customIcons.filter((i) => !knownNames.has(i.name))

const icons = [...lucideIcons, ...uniqueCustom]
await writeSprite(icons)
await writeNamesModule(icons)
await copyFile(
  join(outputDir, 'byz-icons-sprite.svg'),
  join(playgroundPublicDir, 'byz-icons-sprite.svg'),
)

console.log(`Generated sprite with ${icons.length} icons (${lucideIcons.length} Lucide + ${uniqueCustom.length} custom)`)
